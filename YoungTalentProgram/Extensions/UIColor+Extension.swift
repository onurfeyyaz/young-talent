import UIKit

extension UIColor {
    static var blueDark: UIColor {
        return UIColor(named: "Blue - Dark")!
    }
    static var blueLight: UIColor {
        return UIColor(named: "Blue - Light")!
    }
    static var purpleSoft: UIColor {
        return UIColor(named: "Purple - Soft")!
    }
    static var customWhite: UIColor {
        return UIColor(named: "White")!
    }
}
